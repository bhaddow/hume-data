U mírně obézních diabetiků může být operace ztráty hmotnosti užitečná
Přínosy snížení tělesné hmotnosti u mírně obézních osob s diabetem 2. typu může podle nové studie trvat nejméně pět let .
Je ještě brzy říct , zda mírně obézní lidé s cukrovkou žijí déle po operaci s tělesnou ztrátou , než ti , kteří dostávají nechirurgickou léčbu .
" Údaje o úmrtnosti dlouho trvá , než se objeví , " řekl doktor Robin Blackstone , odborník na úbytek váhy , který napsal redakci o nové studii v JAMA Surgery .
Operace na hubnutí , nebo chirurgický zákrok , použijte různé metody , jak zmenšit velikost žaludku .
Snižují hlad a omezují schopnost těla absorbovat potravu .
BMI , míra váhy ve vztahu k výšce , se považuje za normální mezi 18.5 a 24.9 .
BMI 25 nebo vyšší značí , že někdo má nadváhu a lidé s BMI přes 30 jsou považováni za obézní .
Můžete si spočítat své BMI zde : 1.usa.gov / 1D0ZqDv .
Průměrný BMI mezi těmi , kteří měli operaci , klesl z 31 na 24,5 roku do konce jejich pátého ročníku ve studii .
Současně platí , že BMI mezi těmi , kteří dostávali nechirurgickou léčbu cukrovky , zůstal přibližně stejný v 29 .
Mezi těmi , kteří měli operaci , se cukrovka vyřešila zcela v 36 procentech a částečně v 28 procentech .
V lékařsky ošetřené skupině , srovnáním cukrovky , se cukrovka vyřešila zcela pouze v 1 procentu a částečně v pouhých zhruba 2 procentech .
Navíc kontrola krevního tlaku , triglyceridů a " špatného " LDL cholesterolu " byla v chirurgické skupině obecně lepší , " zjistili autoři .
Výzkumníci také monitorovali krevní hladiny hemoglobinu hemoglobin A1C , které v průběhu času odrážejí hladiny cukru v krvi .
Hemoglobin A1c se nejlépe drží pod 7 procenty , píší výzkumníci .
Po operaci se průměrný hemoglobin A1c propadl ze zhruba 9 procent na zhruba 6 procent .
V rámci skupiny Lékařů ale zůstávalo stabilně na zhruba 8 procentech .
Výzkumný tým také porovnával průměrné výsledky se dvěma typy operací .
Ve švédské studii byli obézní lidé , kteří měli chirurgickou chirurgii , o 29 procent méně pravděpodobné , že zemřou v průběhu 15 let , ve srovnání se skupinou , která zkoušela konvenčnější metody .
I když nová studie neprokázala přínos v přežití mezi chirurgickými pacienty , Blackstone řekl , že je uklidňující pro lidi s BMI pod 35 zájemců o postup .
" Právě tady je tento papír kritický , protože tvrdí , že tato operace je bezpečná v té nižší skupině BMI , " bez zvýšeného rizika úmrtí nebo onemocnění ledvin , řekla .
Blackstone dodal , že lidé a jejich lékaři by měli začít brát váhu vážně , jakmile pacient BMI klesne mezi 27 a 30 .
" Myslím , že čekáme příliš dlouho na to , aby to lidé brali vážně , " dodala .
Myslím , že jakmile nahromadily tyto genetické změny , které zatěžují jejich těla do obezity a cukrovky , obrácení je opravdu těžké .
