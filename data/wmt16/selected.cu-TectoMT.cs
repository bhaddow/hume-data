Pro mírně obézní diabetiky operace ztráty hmotnosti může být užitečná
Výhody operace ztráty hmotnosti pro mírně obézní lidi s typem 2 diabetes může trvat málo pěti let podle nové studie.
Je to stále předčasných říci, jestli mírně obézní lidé s cukrovkou žijí dlouho po operaci ztráty hmotnosti než ti, kteří dostávají nechirurgickou léčbu však.
„Údaje o úmrtnosti jsou dlouhý čas ukázat se,“ řekla Dr. Robin Blackstoneová, expert operace ztráty hmotnosti, který napsal úvodník nové studie na JAMA chirurgii.
Operace hmotnostního úbytku nebo bariatrická operace používá různé metody, zmenší velikost žaludku.
Snižují hlad a omezujte schopnost těla absorbovat jídlo.
BMI opatření hmotnosti v oblasti výšky je považován normální mezi 18,5 a 24,9.
BMI 25 nebo vyšší naznačuje, že někdo je nadváha a lidé s BMI nad 30 jsou považováni obezitu.
Můžete vypočítat svůj BMI zde: 1.usa.gov/1d0zqdv.
Průměrná BMI mezi těmi, kteří měla operaci, klesl z 31 na 24,5 o konec jejich pátého roku ve studiu.
Zatím BMI mezi těmi, kteří jsou, nechirurgická léčba cukrovky zůstala o stejné v 29.
Mezi těmi, který měl operaci, diabetes, vyřešil úplně 36 procent a částečně na 28 procent.
V lékařsky ošetřené skupině srovnání diabetes, rozhodl úplně jen 1 procentem a částečně jen asi na 2 procenta.
V navíc, kontrole krevního tlaku, triglyceridech a „špatném“ LDL cholesterol „byl obecně lépe v chirurgické skupině,“ autoři našli.
Pracovníci být sledovali hladiny krve pacientů A1c hemoglobinu, který odráží hladiny krevního cukru průběh času.
Hemoglobin A1c best je držen pod 7 procenty, píší pracovníci.
Po operaci průměrná hladina A1c hemoglobinu klesla z asi 9 procent asi na 6 procent.
V lékařské skupině terapií však zůstalo pevné na asi 8 procentech.
Výzkumný tým také porovnával průměrné výsledky se dvěma typy operace.
Ve švédské studii obézní lidé, kteří měli bariatrickou operaci, byli asi 29 procent málo pravděpodobnosti, že zemře během 15 let, srovnávaly se skupinou, která vyzkoušela konvenčnější metody.
Když nová studie neprokázala dávku v přežití mezi chirurgickými pacienty, Blackstone řekl, uklidňuje pro lidi s BMI základ 35 zúčastněných v řízení.
„Je to, kde tento papír je kritický, Protože se píše, operace není bezpečná v té nižší BMI skupině,“ s žádným zvýšeným rizikem úmrtí nebo ledvinové nemoci, řekla.
Blackstone dodal, že by lidé a jejich lékaři měli začít brát váhu vážně jednou, BMI pacienta spadá mezi 27 a 30.
„Myslím, že čekáme příliš dlouho, máme, lidé jsou vážných o tom,“ řekla.
