Pro mírně obézní diabetiky by mohla být užitečná chirurgie hubnutí
Výhody hubnutí chirurgie pro mírně obézní lidi s diabetem typu 2 můžou trvat nejméně pět let, podle nové studie.
Nicméně je ale ještě příliš brzy na to říci, zda mírně obézní lidi s diabetem typu 2 žijí déle po chirurgii hubnutí než ti, kterým je poskytována nechirurgická léčba.
"Údaje o úmrtnosti trvají dlouho k zobrazení," tvrdí Dr. Robin Blackstone, expert na chirurgii hubnutí, který napsal úvodník na nové studium chirurgie JAMA.
Operace pro hubnutí, bariatrické chirurgie používají pro zmenšení žaludku různé metody.
Snížení hladu a omezení lidského těla absorbovat jídlo.
BMI, které měří vztah váhy vztažené na výšku, je považováno za normální v hodnotách mezi 18,5 a 24,9.
BMI hodnoty 25 nebo vyšší naznačuje nadváhu osoby a lidé s hodnotou nad 30 jsou považováni za obézní.
Můžete si vypočítat své BMI zde: 1.usa.gov/1D0ZqDv.
Průměrné BMI mezi těmi, kteří podstoupili operaci, kleslo podle studie z 31 na 24,5 ke konci jejich pátého roku.
Mezitím zůstalo BMI těch, kterým nebylo poskytnuta chirurgická léčba diabetu, zhruba stejné na hodnotě 29.
Mezi pacienty s operací byl diabetes vyřešen kompletně v 36 procentech a částečně v 28 případech.
V léky ošetřované skupině byl pro porovnání diabetes vyřešen kompletně pouze u 1 procenta a částečně pouze u asi 2 procent pacientů.
Dále také byla kontrola krevního tlaku, trigylceridů a "špatného" LDL cholesterolu prokázána "obecně lepší v chirurgické skupině," zjistili autoři.
Výzkumníci také monitorovali pacientovi úrovně hemoglobinu A1c v krvi, který odráží průběžnou hladinu krevního cukru v těle.
Hemoglobin A1c je nejlépe udržovat pod hladinou 7 procent, uvádí výzkumníci.
Po operaci klesla průměrná hodnota hemoglobinu A1c z asi 9 procent na zhruba 6 procent.
V léky ošetřované skupině však tato hodnota zůstala na asi 8 procentech.
Výzkumný tým také porovnal průměrné výsledky s dvěma typy operací.
Podle švédské studie měli obézní lidé, kteří podstoupili bariatrickou operaci, asi o 29 procent větší šanci na přežití v příštích 15 letech, v porovnání se skupinou, která zkoušela více konvenční metody.
I když nová studie neukázala zlepšení v přežití mezi pacienty po chirurgii, podle Blackstonea je to uklidňující výsledek pro lidi s BMI pod 35, kteří mají zájem o tyto postupy.
"Právě tady je tato práce kritická, protože uvádí, že je chirurgie bezpečnějších ve skupině s nižším BMI," bez zvýšení rizika smrti nebo onemocnění ledvin, uvádí.
Blackstone také dále tvrdí, že lidé a jejich doktoři by měli začít brát váhu opravdu vážně po poklesu pacientova BMI mezi 27 a 30.
