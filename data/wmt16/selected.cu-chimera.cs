To je ještě brzy na to říct, zda se mírně obézní lidé s cukrovkou žít déle po operaci ztráty hmotnosti než ti, kteří dostávají neinvazivní léčby, ale...
„Údaje o úmrtnosti trvat dlouho, než se objeví,“ řekl doktor Robin Blackstone, odborník na hubnutí chirurgický zákrok, který napsal úvodník nová studie v JAMA chirurgii.
Úbytek na váze, operace nebo bariatrické operaci, používají různé metody, aby zmenší velikost žaludku.
Snižují hlad a omezují schopnost těla absorbovat jídlo.
BMI, jistou míru váhy ve vztahu k výšce, bylo považováno za normální mezi 18,5 a 24.9.
BMI 25 nebo vyšší, naznačuje, že někdo má nadváhu, a lidé s BMI nad 30 jsou obézní.
Zde můžete spočítat svůj BMI: 1. usa. gov / 1 d 0 zqdv.
Průměrná BMI mezi těmi, kteří měli operaci, klesl z 31 na 24,5 na konci pátého roku v pracovně.
Mezitím BMI mezi těmi nechirurgická léčba cukrovky zůstala zhruba stejná na 29.
Mezi těmi, kteří operovali, cukrovka odezněly v 36 procent a částečně na 28 procent.
Z lékařského hlediska je ve skupině, ve srovnání s cukrovkou odezněly v pouze 1% a částečně jen o 2 procenta.
Kromě kontroly krevního tlaku, hladiny triglyceridů, a „zlý“ LDL cholesterol „byl obecně lépe v chirurgické skupině,“ autoři našli.
Výzkumníci sledovali pacientů také hladiny hemoglobinu a 1 c, které odrážejí hladinu cukru v krvi.
A 1 c, je lepší nechat pod 7 procent, píší pracovníci.
Po operaci je průměrná úroveň a 1 c spadl z asi 9 procent na zhruba 6 procent.
V lékařské terapii však zůstalo stabilní na asi 8 procent.
Výzkumný tým také průměrné výsledky ve srovnání se dvěma typy operací.
Ve švédské studie, obézní lidé, kteří měli bariatrickou operaci byly o 29 procent nižší pravděpodobnost, že zemře během 15 let, ve srovnání se skupinou, která se snažila více konvenční metody.
I když nová studie neprokázala přínos v přežití mezi pacienty operovat, Blackstone řekl, že to je uklidňující pro lidi s BMI pod 35 let zájem o postup.
